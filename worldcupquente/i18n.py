def normalize_language(language: str | None) -> str:
    return language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def text(key: str, language: str | None = DEFAULT_LANGUAGE, **kwargs: object) -> str:
    normalized = normalize_language(language)
    value = TEXT[normalized].get(key, TEXT[DEFAULT_LANGUAGE][key])
    return value.format(**kwargs) if kwargs else value


def status_text(status: str, language: str | None = DEFAULT_LANGUAGE) -> str:
    normalized = normalize_language(language)
    return STATUS_TRANSLATIONS[normalized].get(status, status)


def format_duration(parts: list[tuple[int, str]], language: str | None = DEFAULT_LANGUAGE) -> str:
    normalized = normalize_language(language)
    labels = {
        "en": {
            "day": ("day", "days"),
            "hour": ("hour", "hours"),
            "minute": ("minute", "minutes"),
            "and": "and",
        },
        "pt": {
            "day": ("dia", "dias"),
            "hour": ("hora", "horas"),
            "minute": ("minuto", "minutos"),
            "and": "e",
        },
    }[normalized]
    formatted = [
        f"{value} {labels[unit][0] if value == 1 else labels[unit][1]}" for value, unit in parts
    ]
    if len(formatted) == 1:
        return formatted[0]
    if len(formatted) == 2:
        return f"{formatted[0]} {labels['and']} {formatted[1]}"
    return f"{formatted[0]}, {formatted[1]} {labels['and']} {formatted[2]}"
